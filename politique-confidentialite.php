<p>
  <font color="black"> 
<h3>1. Préambule</h3>
La présente politique de protection des données a pour objectif de vous informer sur les engagements et mesures pris par la société ABC ÉNERGIES  (ci-après « ABC ÉNERGIES  ») afin de veiller au respect de vos données à caractère personnel.<br />
Cette politique concerne les traitements réalisés dans le cadre de la gestion du site internet energiespourtous.fr ainsi que des demandes transmises à partir des formulaires de contact.<br />
 ABC ÉNERGIES  est le responsable du traitement de vos données personnelles collectées sur le site précité. Vous pouvez trouver l’ensemble des informations concernant la société  ABC ÉNERGIES  sur la page mentions légales accessibles sur le site <b>https://energiespourtous.fr/mentions-legales/</b>.<br />
La présente politique de confidentialité pourra évoluer en fonction du contexte légal et réglementaire et des recommandations de la Commission Nationale de l’Informatique et des Libertés.
<br />
<p>
  <font color="black"> 
<h3>2. Définitions</h3>
ABC ÉNERGIES  veille à collecter et à traiter des données à caractère personnel pertinentes, adéquates, non excessives et strictement nécessaires à l’atteinte des finalités qui ont été préalablement déterminées. ABC ÉNERGIES  prend toutes les dispositions utiles pour que vos données soient exactes, complètes et, si nécessaire, mises à jour. ABC ÉNERGIES  ne partage jamais vos données personnelles avec d’autres entreprises à des fins de prospection commerciale. Les données personnelles que ABC ÉNERGIES  collecte sont uniquement composées des données nécessaires à la réalisation des finalités des traitements mis en oeuvre. ABC ÉNERGIES  s’engage à ne pas collecter plus de données que nécessaire. Dans le cadre de l’utilisation du site internet energiespourtous.fr, ABC ÉNERGIES  collecte et traite les informations fournies sur le formulaire à savoir l’identité, l’adresse mail, les coordonnées téléphoniques et le cas échéant, le contenu du message ainsi que toute information communiquée ultérieurement dans le cadre de ces échanges. Les données sont collectées et traitées par ABC ÉNERGIES  ou ses sous-traitants techniques et commerciaux aux seules fins énoncées ci-après. Les données ne sont pas susceptibles de transfert hors du territoire de l’Union européenne.
</p>
<p>
  <font color="black"> 
<h3>3. Données collectées</h3>
Dans le cadre de la visite du Site, aucune information personnelle n’est à fournir. Toutefois, dans certaines situations et notamment concernant la prise de renseignement via notre formulaire de contact, certaines de vos données personnelles (nom, prénom, adresse électronique et numéro de téléphone) peuvent être collectées. Ce traitement est néanmoins nécessaire à l’exécution de la prestation de ABC ÉNERGIES. Aussi, les données ainsi transmises pourront être utilisées par ABC ÉNERGIES afin de vous apporter une réponse la plus précise possible. Ces données sont uniquement utilisées à cette fin et ne seront pas communiquées à des tiers par ABC ÉNERGIES. En tout état de cause, en naviguant sur le Site, le visiteur accepte la gestion de ses données personnelles ainsi que la politique de confidentialité présentement décrites. Pour plus de précisions, se reporter à la politique de ABC ÉNERGIES en matière de protection des données personnelles du présent Site.
</p>

<p>
  <font color="black"> 
<h3>4. Finalités de la collecte</h3>
ABC ÉNERGIES  collecte vos données pour les finalités suivantes :<br /><br />
- répondre à une demande d'éligibilité formulée par une personne visitant le site internet ;<br />
- améliorer le site internet, nos produits et services (notamment par le biais des cookies) ;<br />
- élaborer des statistiques et des mesures de fréquentation des services.<br /><br />
Ces données ne peuvent être utilisées ultérieurement de manière incompatible avec ces finalités. Ces données sont collectées loyalement ; aucune collecte n’est effectuée à l’insu des personnes et sans qu’elles en soient informées.
</p>
<p>
  <font color="black"> 
<h3>5. Durée de conservation limitée des données à caractère personnel</h3>
Les données à caractère personnel sont conservées pendant une durée limitée qui n’excède pas la durée nécessaire aux finalités poursuivies et ne saurait en tout état de cause excéder 24 mois.<br />
ABC ÉNERGIES  peut cependant conserver vos données à caractère personnel pour une durée plus longue pour se conformer aux obligations légales et notamment aux délais de prescription applicables.
</p>

  <font color="black"> 
<h3>6. Confidentialité / Sécurité des données</h3>
Des mesures de sécurité physiques, logistiques et organisationnelles appropriées sont prévues pour garantir la confidentialité des données, et notamment éviter tout accès non autorisé.
</p>

  <font color="black"> 
<h3>7.  Destinataires des données</h3>
ABC ÉNERGIES  ne communique vos données à caractère personnel qu’à des destinataires habilités et déterminés.<br />
ABC ÉNERGIES  peut notamment donner accès à vos données à caractère personnel à ses partenaires ainsi qu’à ses éventuels prestataires de services tiers, agissant en tant que sous-traitants, pour exécuter des prestations relatives aux services et aux applications informatiques de l’entreprise (notamment des services d’hébergement, de stockage, d’analyses, de traitement de données, de gestion de bases de données ou encore de maintenance informatique).
Ces prestataires tiers, qui n’agissent que sur instruction de ABC ÉNERGIES , n’auront accès à vos données à caractère personnel que pour exécuter les finalités poursuivies au moment de la collecte desdites données et seront tenus aux mêmes obligations de sécurité et de confidentialité que l’entreprise.
Par ailleurs, vos données à caractère personnel peuvent être partagées pour les motifs suivants :<br /><br />
- En réponse à une procédure judiciaire ou administrative de toute nature ou à des mesures d’application de la loi réclamées par les autorités compétentes ;<br />
- Pour se conformer à des obligations légales, pour protéger les droits et/ou la sûreté d’un individu, pour protéger les droits et la propriété de ABC ÉNERGIES , y compris la nécessité de voir la présente Politique de Protection des Données respectée et d’empêcher les problèmes de fraude, de sécurité ou techniques.
</p>

  <font color="black"> 
<h3>8. Vos droits</h3>
Conformément à la législation applicable en matière de protection des données à caractère personnel, vous disposez d’un certain nombre de droits concernant la collecte et le traitement de vos données à caractère personnel, à savoir :<br /><br />
<br /><br /><b>Le droit d’être informé :</b> Vous avez le droit d’être informé d’une façon concise, transparente, compréhensible et aisément accessible de lamanière dont vos données à caractère personnel sont traitées.
<br /><br /><b>Le droit d’accès :</b> Vous avez le droit d’obtenir la confirmation que des données à caractère personnel vous concernant sont ou ne sont pas traitées et, lorsqu’elles le sont, d’obtenir l’accès auxdites données et une copie de ces dernières.
<br /><br /><b>Le droit de rectification :</b> Vous avez le droit d’obtenir la rectification des données à caractère personnel vous concernant qui sont inexactes. Vous avez également le droit d’obtenir que les données à caractère personnel incomplètes soient complétées, y compris en fournissant une déclaration complémentaire.
<br /><br /><b>Le droit à l’effacement :</b> Dans certains cas, vous avez le droit d’obtenir l’effacement de vos données à caractère personnel. Ce droit n’est cependant pas un droit absolu et ABC ÉNERGIES  peut avoir des raisons légales ou légitimes de conserver lesdites données.
<br /><br /><b>Le droit à la limitation du traitement :</b> Dans certains cas, vous avez le droit d’obtenir la limitation du traitement de vos données à caractère personnel.
<br /><br /><b>Le droit à la portabilité :</b> Vous avez le droit de recevoir les données à caractère personnel vous concernant fournies à ABC ÉNERGIES  et vous avez le droit de transmettre ces données à un autre responsable de traitement sans que ABC ÉNERGIES  y fasse obstacle. Ce droit ne s’applique que lorsque le traitement de vos données à caractère personnel est fondé sur votre consentement ou sur l’exécution d’un contrat et que ledit traitement est effectué à l’aide de procédés automatisés.
<br /><br /><b>Le droit d’opposition :</b> Vous avez le droit de vous opposer à tout moment, pour des raisons tenant à votre situation particulière, à un traitement de données à caractère personnel vous concernant lorsque ledit traitement est fondé sur l’intérêt légitime de ABC ÉNERGIES . ABC ÉNERGIES  peut cependant invoquer des motifs légitimes et impérieux nécessitant la poursuite du traitement.
<br /><br /><b>Le droit d’introduire une réclamation auprès d’une autorité de contrôle :</b> Vous avez le droit de contacter une Autorité de contrôle (telle que la CNIL) afin d’introduire une réclamation concernant les pratiques relatives à la protection des données à caractère personnel de ABC ÉNERGIES .
<br /><br /><b>Le droit de transmettre des instructions concernant l’utilisation des données après le décès :</b> vous avez le droit de donner à ABC ÉNERGIES  des directives concernant l’utilisation de vos données à caractère personnel après votre décès.
<br /><br />
<b>Pour exercer ces droits, vous pouvez adresser une demande :</b><br />
<br />
<u>par courriel</u> à : dpo@energiespourtous.fr;<br />
<u>par voie postale</u> à : 1B Rue Danièle Casaova 26100 Romans-sur-Isère, France.

</p>

  <font color="black"> 
<h3>9. Contact</h3>
Pour toutes demandes d’informations concernant la politique de protection des données à caractère personnel mise en œuvre par ABC ÉNERGIES , vous pouvez vous adresser à dpo@energiespourtous.fr

</p>

  <font color="black"> 
<h3>10. Mise à jour</h3>
Cette Politique peut être mise à jour à tout moment, aussi nous vous conseillons de la consulter régulièrement.
</p>

